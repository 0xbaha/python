print("for i in range(1,1)")
for i in range(1,1):
    print(i)
print()
print("for i in range(3,3)")
for i in range(3,3):
    print(i)
print()
print("for i in range(1,5):")
for i in range(1,5):
    print(i)
print()
print("for i in range(2,5):")
